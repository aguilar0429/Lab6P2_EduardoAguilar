    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        String title ="";
        try{
            title = JTF_title.getText();
            AdministrarNotas an = new AdministrarNotas("./" + title + ".txt");
            an.cargarArchivo();

            Date creation = new Date();
            SimpleDateFormat formatter = new SimpleDateFormat("dd-M-yyyy hh:mm:ss");
            String fecha = formatter.format(creation);

            String priority = "";
            if (ALTA.isSelected()) {
                priority = "Alta";
            } else if (BAJA.isSelected()) {
                priority = "Baja";
            } else if (MEDIA.isSelected()) {
                priority = "Media";
            }
            String content = TA_contenido.getText();
            Notas n = new Notas(title, fecha, priority, content, usuario);
            an.getLista().add(n);
            
            an.escribirArchivo();
            
            
        }catch(IOException e){
            
        }
        JTF_title.setText("");
        TA_contenido.setText("");
        General.setVisible(false);
        DefaultComboBoxModel dc = (DefaultComboBoxModel) this.cb_notas.getModel();
        dc.addElement(title);
        cb_notas.setModel(dc);
        
       
        
    }//GEN-LAST:event_jButton5MouseClicked

